require "spec_helper"

describe HeatGroupsController do
  describe "routing" do

    it "recognizes and generates #index" do
      { :get => "/heat_groups" }.should route_to(:controller => "heat_groups", :action => "index")
    end

    it "recognizes and generates #new" do
      { :get => "/heat_groups/new" }.should route_to(:controller => "heat_groups", :action => "new")
    end

    it "recognizes and generates #show" do
      { :get => "/heat_groups/1" }.should route_to(:controller => "heat_groups", :action => "show", :id => "1")
    end

    it "recognizes and generates #edit" do
      { :get => "/heat_groups/1/edit" }.should route_to(:controller => "heat_groups", :action => "edit", :id => "1")
    end

    it "recognizes and generates #create" do
      { :post => "/heat_groups" }.should route_to(:controller => "heat_groups", :action => "create")
    end

    it "recognizes and generates #update" do
      { :put => "/heat_groups/1" }.should route_to(:controller => "heat_groups", :action => "update", :id => "1")
    end

    it "recognizes and generates #destroy" do
      { :delete => "/heat_groups/1" }.should route_to(:controller => "heat_groups", :action => "destroy", :id => "1")
    end

  end
end
